    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
       
        try
        {
        Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/school","root","root");
            String s ="select * from student inner join result where student.rollnumber= '"+rollnumber+"' and result.rollnumber= '"+rollnumber+"'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(s);
            if(rs.next())
            {
            int stclass = rs.getInt(2);
            String name= rs.getString(3);
            String dob = rs.getString(4);
            String gender = rs.getString(5);
            String fathername= rs.getString(6);
            txtrollnumber.setText(rollnumber);
            int kannada = rs.getInt(8);
            int english = rs.getInt(9);
            int hindi = rs.getInt(10);
            int mathematics= rs.getInt(11);
            int science = rs.getInt(12);
            int social = rs.getInt(13);
            
            
            txtclass.setText(String.valueOf(stclass));
            txtname.setText(name);
            txtdob.setText(dob);
            txtgender.setText(gender);
            txtfathername.setText(fathername);
            txtkannada.setText(String.valueOf(kannada));
            txtenglish.setText(String.valueOf(english));
            txthindi.setText(String.valueOf(hindi));
            txtmathematics.setText(String.valueOf(mathematics));
            txtscience.setText(String.valueOf(science));
            txtsocial.setText(String.valueOf(social));
            
            int total = kannada+english+hindi+mathematics+science+social;
            
            txttotalmarks.setText(String.valueOf(total));
            
            if(kannada<35 ||english<35 || hindi <35 || mathematics<35 || science<35 || social<35)
              {
                txtresult.setText("Fail");
                txtresult.setBackground(Color.red);
              }
            else
              {
                txtresult.setText("Pass");
                txtresult.setBackground(Color.green);
               }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"No Data");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

